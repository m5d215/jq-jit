/// Per-opcode metadata: (flags, base_length) pairs.
///
/// Derived directly from the C macro definitions in bytecode.c:
///
/// ```c
/// #define NONE 0, 1
/// #define CONSTANT OP_HAS_CONSTANT, 2
/// #define VARIABLE (OP_HAS_VARIABLE | OP_HAS_BINDING), 3
/// #define GLOBAL   (OP_HAS_CONSTANT | OP_HAS_VARIABLE | OP_HAS_BINDING | OP_IS_CALL_PSEUDO), 4
/// #define BRANCH   OP_HAS_BRANCH, 2
/// #define CFUNC    (OP_HAS_CFUNC | OP_HAS_BINDING), 3
/// #define UFUNC    (OP_HAS_UFUNC | OP_HAS_BINDING | OP_IS_CALL_PSEUDO), 4
/// #define DEFINITION (OP_IS_CALL_PSEUDO | OP_HAS_BINDING), 0
/// #define CLOSURE_REF_IMM (OP_IS_CALL_PSEUDO | OP_HAS_BINDING), 2
/// ```
fn opcode_meta(op: Opcode) -> (u32, usize) {
    // Returns (flags, base_length) matching the C opcode_descriptions table.
    //
    // Note: CALL_JQ/TAIL_CALL_JQ have variable length: base + code[pc+1]*2
    // (handled by opcode_length_with_code).
    match op {
        // NONE -> (0, 1)
        Opcode::DUP => (0, 1),
        Opcode::DUPN => (0, 1),
        Opcode::DUP2 => (0, 1),
        Opcode::POP => (0, 1),
        Opcode::INDEX => (0, 1),
        Opcode::INDEX_OPT => (0, 1),
        Opcode::EACH => (0, 1),
        Opcode::EACH_OPT => (0, 1),
        Opcode::TRY_END => (0, 1),
        Opcode::BACKTRACK => (0, 1),
        Opcode::INSERT => (0, 1),
        Opcode::SUBEXP_BEGIN => (0, 1),
        Opcode::SUBEXP_END => (0, 1),
        Opcode::PATH_BEGIN => (0, 1),
        Opcode::PATH_END => (0, 1),
        Opcode::RET => (0, 1),
        Opcode::TOP => (0, 1),
        Opcode::GENLABEL => (0, 1),

        // CONSTANT -> (OP_HAS_CONSTANT, 2)
        Opcode::LOADK => (OP_HAS_CONSTANT, 2),
        Opcode::PUSHK_UNDER => (OP_HAS_CONSTANT, 2),
        Opcode::DEPS => (OP_HAS_CONSTANT, 2),
        Opcode::MODULEMETA => (OP_HAS_CONSTANT, 2),
        Opcode::ERRORK => (OP_HAS_CONSTANT, 2),

        // VARIABLE -> (OP_HAS_VARIABLE | OP_HAS_BINDING, 3)
        Opcode::LOADV => (OP_HAS_VARIABLE | OP_HAS_BINDING, 3),
        Opcode::LOADVN => (OP_HAS_VARIABLE | OP_HAS_BINDING, 3),
        Opcode::STOREV => (OP_HAS_VARIABLE | OP_HAS_BINDING, 3),
        Opcode::APPEND => (OP_HAS_VARIABLE | OP_HAS_BINDING, 3),
        Opcode::RANGE => (OP_HAS_VARIABLE | OP_HAS_BINDING, 3),
        Opcode::STOREVN => (OP_HAS_VARIABLE | OP_HAS_BINDING, 3),

        // GLOBAL -> (OP_HAS_CONSTANT | OP_HAS_VARIABLE | OP_HAS_BINDING | OP_IS_CALL_PSEUDO, 4)
        Opcode::STORE_GLOBAL => (
            OP_HAS_CONSTANT | OP_HAS_VARIABLE | OP_HAS_BINDING | OP_IS_CALL_PSEUDO,
            4,
        ),

        // BRANCH -> (OP_HAS_BRANCH, 2)
        Opcode::FORK => (OP_HAS_BRANCH, 2),
        Opcode::TRY_BEGIN => (OP_HAS_BRANCH, 2),
        Opcode::JUMP => (OP_HAS_BRANCH, 2),
        Opcode::JUMP_F => (OP_HAS_BRANCH, 2),
        Opcode::DESTRUCTURE_ALT => (OP_HAS_BRANCH, 2),

        // CFUNC -> (OP_HAS_CFUNC | OP_HAS_BINDING, 3)
        Opcode::CALL_BUILTIN => (OP_HAS_CFUNC | OP_HAS_BINDING, 3),

        // UFUNC -> (OP_HAS_UFUNC | OP_HAS_BINDING | OP_IS_CALL_PSEUDO, 4)
        Opcode::CALL_JQ => (OP_HAS_UFUNC | OP_HAS_BINDING | OP_IS_CALL_PSEUDO, 4),
        Opcode::TAIL_CALL_JQ => (OP_HAS_UFUNC | OP_HAS_BINDING | OP_IS_CALL_PSEUDO, 4),

        // DEFINITION -> (OP_IS_CALL_PSEUDO | OP_HAS_BINDING, 0)
        // These pseudo-instructions have length 0 in the description table,
        // but they don't appear independently in the bytecode stream.
        // They are part of CALL_JQ's variable-length encoding.
        Opcode::CLOSURE_PARAM => (OP_IS_CALL_PSEUDO | OP_HAS_BINDING, 0),
        Opcode::CLOSURE_CREATE => (OP_IS_CALL_PSEUDO | OP_HAS_BINDING, 0),
        Opcode::CLOSURE_CREATE_C => (OP_IS_CALL_PSEUDO | OP_HAS_BINDING, 0),
        Opcode::CLOSURE_PARAM_REGULAR => (OP_IS_CALL_PSEUDO | OP_HAS_BINDING, 0),

        // CLOSURE_REF_IMM -> (OP_IS_CALL_PSEUDO | OP_HAS_BINDING, 2)
        Opcode::CLOSURE_REF => (OP_IS_CALL_PSEUDO | OP_HAS_BINDING, 2),
    }
}

/// Opcode flags for each opcode (mirrors opcode_describe()->flags).
pub fn opcode_flags(op: Opcode) -> u32 {
    opcode_meta(op).0
}

/// Base instruction length (in 16-bit words), NOT including CALL_JQ's variable part.
///
/// For CALL_JQ and TAIL_CALL_JQ, the actual length depends on the first immediate
/// (nargs): `base_length + code[pc+1] * 2`.  Use [`opcode_length_with_code`] instead.
pub fn opcode_base_length(op: Opcode) -> usize {
    opcode_meta(op).1
}

/// Instruction length including variable-length CALL_JQ encoding.
///
/// `code` is the slice starting at the opcode word.
pub fn opcode_length_with_code(op: Opcode, code: &[u16]) -> usize {
    let base = opcode_base_length(op);
    if (op == Opcode::CALL_JQ || op == Opcode::TAIL_CALL_JQ) && code.len() > 1 {
        // code[1] = nargs; each arg takes 2 words (level + idx)
        let nargs = code[1] as usize;
        base + nargs * 2
    } else {
        base
    }
}
