//! Raw FFI bindings for libjq.
//!
//! This module provides `#[repr(C)]` type definitions that mirror jq's internal
//! C structures, plus `extern "C"` declarations for the public API functions we need.
//!
//! # Safety
//!
//! All types and functions in this module are `unsafe` by nature.  They should only
//! be used through the safe wrappers in `crate::bytecode`.
//!
//! # Layout notes
//!
//! - `jv` is a 16-byte tagged union (jv.h).  We treat it as opaque and access it
//!   only via the `jv_*` FFI functions.
//! - `struct bytecode` is defined in jq's internal `bytecode.h`.  Since it is not
//!   in the public header, we provide a hand-written `#[repr(C)]` definition that
//!   must match jq 1.8.1 exactly.
//! - `jq_state` is opaque.  We extract the `bytecode*` field by computing its
//!   offset within the struct (see `jq_state_get_bytecode`).

use std::ffi::c_char;
use std::ffi::c_int;

// ---------------------------------------------------------------------------
// jv type (jv.h)
// ---------------------------------------------------------------------------

/// jq value kind tags (from jv.h `jv_kind` enum).
#[repr(C)]
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum JvKind {
    Invalid = 0,
    Null = 1,
    False = 2,
    True = 3,
    Number = 4,
    String = 5,
    Array = 6,
    Object = 7,
}

/// Opaque reference-counted pointer used inside `jv`.
#[repr(C)]
pub struct JvRefcnt {
    _opaque: [u8; 0],
}

/// The `jv` tagged union.  16 bytes on all 64-bit platforms.
///
/// ```c
/// typedef struct {
///   unsigned char kind_flags;
///   unsigned char pad_;
///   unsigned short offset;
///   int size;
///   union { struct jv_refcnt* ptr; double number; } u;
/// } jv;
/// ```
#[repr(C)]
#[derive(Clone, Copy)]
pub struct Jv {
    pub kind_flags: u8,
    pub pad_: u8,
    pub offset: u16,
    pub size: i32,
    pub u: JvPayload,
}

/// Payload union of `jv`: either a heap pointer or an inline double.
#[repr(C)]
#[derive(Clone, Copy)]
pub union JvPayload {
    pub ptr: *mut JvRefcnt,
    pub number: f64,
}

// Verify that jv is exactly 16 bytes (compile-time assertion).
const _: () = assert!(std::mem::size_of::<Jv>() == 16);

