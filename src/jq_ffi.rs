/// C function pointer union — we only need the raw pointer for identification.
#[repr(C)]
pub union CFunctionPtr {
    pub a1: unsafe extern "C" fn(*mut JqState, Jv) -> Jv,
    pub a2: unsafe extern "C" fn(*mut JqState, Jv, Jv) -> Jv,
    pub a3: unsafe extern "C" fn(*mut JqState, Jv, Jv, Jv) -> Jv,
    pub a4: unsafe extern "C" fn(*mut JqState, Jv, Jv, Jv, Jv) -> Jv,
}

/// `struct cfunction` from bytecode.h.
#[repr(C)]
pub struct CFunction {
    pub fptr: CFunctionPtr,
    pub name: *const c_char,
    pub nargs: c_int,
}

/// `struct symbol_table` from bytecode.h.
#[repr(C)]
pub struct SymbolTable {
    pub cfunctions: *mut CFunction,
    pub ncfunctions: c_int,
    pub cfunc_names: Jv, // 16 bytes
}

/// `struct bytecode` from bytecode.h (jq 1.8.1).
///
/// This must have the exact same memory layout as the C struct.
#[repr(C)]
pub struct Bytecode {
    pub code: *mut u16,
    pub codelen: c_int,
    pub nlocals: c_int,
    pub nclosures: c_int,
    // 4 bytes padding here (to align `constants` to 8 bytes)
    pub constants: Jv, // 16 bytes
    pub globals: *mut SymbolTable,
    pub subfunctions: *mut *mut Bytecode,
    pub nsubfunctions: c_int,
    // 4 bytes padding here (to align `parent` to 8 bytes)
    pub parent: *mut Bytecode,
    pub debuginfo: Jv, // 16 bytes
}

// ---------------------------------------------------------------------------
// jq_state — opaque, accessed via offset
// ---------------------------------------------------------------------------

/// Opaque `jq_state` handle.  We never construct this directly.
#[repr(C)]
pub struct JqState {
    _opaque: [u8; 0],
}

/// Offset of the `bc` field within `struct jq_state`.
///
/// ```c
/// struct jq_state {
///   void (*nomem_handler)(void *);   // 0: 8 bytes (fn ptr)
///   void *nomem_handler_data;        // 8: 8 bytes (ptr)
///   struct bytecode* bc;             // 16: 8 bytes (ptr)  <-- this
///   ...
/// };
/// ```
const JQ_STATE_BC_OFFSET: usize = 16;

/// Extract the `struct bytecode*` from a `jq_state*`.
///
/// # Safety
///
/// `state` must be a valid, non-null pointer returned by `jq_init()` after a
/// successful `jq_compile()` call.
pub unsafe fn jq_state_get_bytecode(state: *mut JqState) -> *mut Bytecode {
    unsafe {
        let base = state as *const u8;
        let bc_ptr_location = base.add(JQ_STATE_BC_OFFSET) as *const *mut Bytecode;
        *bc_ptr_location
    }
}

